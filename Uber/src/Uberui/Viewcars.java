    private void CatalogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CatalogActionPerformed
        // TODO add your handling code here:
        ArrayList<Date> dateList = new ArrayList<Date>();
        SimpleDateFormat formatter = new SimpleDateFormat("MM-dd-yyyy hh:mm:ss a");
        for (Cars cars: hist.getHist()) {

            if(cars.getDate() != null){
                dateList.add(cars.getDate());
            }
        }
        if(dateList.size()>0){
            String maxDate = formatter.format(Collections.max(dateList));
            updateRefresh.setText(maxDate);
        }
        else{
            updateRefresh.setText("Never Updated");
        }

    }//GEN-LAST:event_CatalogActionPerformed


