    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) Carstbl.getModel();
        model.setRowCount(0);
        
          for(Cars cars: hist.getHist()){
            Object[] row = new Object[6];
            row[0] = cars;
            row[1] = cars.getManufacturer();
            row[2] = cars.getCity();
            row[3] = cars.getSerialno();
            row[4] = cars.isAvailability();
            row[5] = cars.getDate();
    
            model.addRow(row);
            
        }
        }
