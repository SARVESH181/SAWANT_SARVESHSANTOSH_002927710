    private void ViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewActionPerformed
        // TODO add your handling code here:
        int selected = Carstbl.getSelectedRow();
        if(selected < 0){
            JOptionPane.showMessageDialog(this,"Please selected the row to be viewed");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) Carstbl.getModel();
        Cars selectedCar = (Cars) model.getValueAt(selected,0);
        name.setText(selectedCar.getCarname());
        manufacturer.setText(selectedCar.getManufacturer());
        serialno.setText(selectedCar.getSerialno());
        city.setText(selectedCar.getCity());
        modelno.setText(selectedCar.getModelno());
        year.setText(String.valueOf(selectedCar.getYear()));
        Maintainencevalid.setSelected(selectedCar.isMaintainencevalid());
        company.setText(selectedCar.getCarcompany());
        seats2.setText(String.valueOf(selectedCar.getSeatno()));
        availability.setSelected(selectedCar.isAvailability());
    }//GEN-LAST:event_ViewActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int selected = Carstbl.getSelectedRow();
        if(selected < 0){
            JOptionPane.showMessageDialog(this,"Please selected the row to be deleted");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) Carstbl.getModel();
        Cars selectedCar = (Cars) model.getValueAt(selected,0);
        hist.deleteDetails(selectedCar);
        JOptionPane.showMessageDialog(this,"Car Record Deleted");
        populateTable();

    }//GEN-LAST:event_DeleteActionPerformed

    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:

        int selected = Carstbl.getSelectedRow();
        if(selected < 0){
            JOptionPane.showMessageDialog(this,"Please selected the row to be viewed");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) Carstbl.getModel();
        Cars selectedCar = (Cars) model.getValueAt(selected,0);
        selectedCar.setCity(city.getText());
        boolean mainvalid ;
        if(Maintainencevalid.isSelected()){
            mainvalid = true;
        }
        else{
            mainvalid = false;
        }
        selectedCar.setCarcompany(company.getText());
        boolean available ;
        if(availability.isSelected()){
            available = true;
        }
        else{
            available = false;
        }
        selectedCar.setAvailability(available);
        updateStatus.setText("");//date
        Date date = new Date();
        SimpleDateFormat formatter = new SimpleDateFormat("MM-dd-yyyy hh:mm:ss a");
        updateStatus.setText("Last Updated on : " + date);
        selectedCar.setDate(date);
        refreshTable();
    }//GEN-LAST:event_SaveActionPerformed
