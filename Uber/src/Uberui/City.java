    private void populateRecords(String city1) {
        DefaultTableModel model = (DefaultTableModel) citytbl.getModel();
        model.setRowCount(0);
        
        for(Cars cars: hist.getHist()){
            Object[] row = new Object[8];
            if(cars.getCity().toLowerCase().equals(city1.toLowerCase())){
            row[0] = cars;
            row[1] = cars.getCarcompany();
            row[2] = cars.getYear();
            row[3] = cars.getSerialno();
            row[4] = cars.getModelno();
            row[5] = cars.getManufacturer();
            row[6] = cars.getSeatno();
            row[7] = cars.isMaintainencevalid();
            model.addRow(row);
                 }
        }
        }
